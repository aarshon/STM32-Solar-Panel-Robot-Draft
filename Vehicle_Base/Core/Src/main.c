/*
 * processCommand
 * Parses a complete command string and drives motors + LCD accordingly.
 */
static void processCommand(const char *cmd)
{
    lastCmdTime = HAL_GetTick();

    if (strncmp(cmd, "ESTOP", 5) == 0)
    {
        VESC_Stop();
        motorRunning = 0;
        UI_ForceRedraw();
        HAL_UART_Transmit(&huart3, (uint8_t *)"[BASE] ESTOP\r\n", 14, 20);
        return;
    }

    if (strncmp(cmd, "STP", 3) == 0)
    {
        VESC_Stop();
        motorRunning = 0;
        UI_ForceRedraw();
        HAL_UART_Transmit(&huart3, (uint8_t *)"[BASE] Stop\r\n", 13, 20);
        return;
    }

    uint8_t speed = 150;
    const char *comma = strchr(cmd, ',');
    if (comma != NULL)
    {
        int v = atoi(comma + 1);
        if (v >= 0 && v <= 255) speed = (uint8_t)v;
    }

    char speedStr[17];
    snprintf(speedStr, sizeof(speedStr), "Speed: %d", speed);

    char dbg[40];

    if (strncmp(cmd, "FWD", 3) == 0)
    {
        VESC_TankDrive(DIR_FORWARD, speed);
        motorRunning = 1;
        UI_ForceRedraw();
        WriteStatus(">>> FORWARD >>>", speedStr);
        snprintf(dbg, sizeof(dbg), "[BASE] FWD spd=%d\r\n", speed);
    }
    else if (strncmp(cmd, "BCK", 3) == 0)
    {
        VESC_TankDrive(DIR_BACKWARD, speed);
        motorRunning = 1;
        UI_ForceRedraw();
        WriteStatus("<<< BACKWARD <<", speedStr);
        snprintf(dbg, sizeof(dbg), "[BASE] BCK spd=%d\r\n", speed);
    }
    else if (strncmp(cmd, "LFT", 3) == 0)
    {
        VESC_TankDrive(DIR_LEFT, speed);
        motorRunning = 1;
        UI_ForceRedraw();
        WriteStatus("<-- TURN LEFT  ", speedStr);
        snprintf(dbg, sizeof(dbg), "[BASE] LFT spd=%d\r\n", speed);
    }
    else if (strncmp(cmd, "RGT", 3) == 0)
    {
        VESC_TankDrive(DIR_RIGHT, speed);
        motorRunning = 1;
        UI_ForceRedraw();
        WriteStatus("  TURN RIGHT -->", speedStr);
        snprintf(dbg, sizeof(dbg), "[BASE] RGT spd=%d\r\n", speed);
    }
    else
    {
        snprintf(dbg, sizeof(dbg), "[BASE] Unknown: %.20s\r\n", cmd);
    }

    HAL_UART_Transmit(&huart3, (uint8_t *)dbg, strlen(dbg), 20);
}

