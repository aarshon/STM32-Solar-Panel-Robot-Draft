  ssd1306_Init();
  KEYPAD_Init();
  UI_Init();

  VESC_SetValuesCallback(my_vesc_callback);
  HAL_UART_Receive_IT(&huart1, &rxByte, 1);
  HAL_UART_Receive_IT(&huart2, &vescRxByte, 1);
