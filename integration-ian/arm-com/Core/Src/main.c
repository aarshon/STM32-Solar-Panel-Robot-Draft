/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ETH_Init();
  MX_USART3_UART_Init();
  MX_USB_OTG_FS_PCD_Init();
  MX_I2C1_Init();
  MX_TIM1_Init();
  MX_TIM9_Init();
  MX_UART4_Init();
  /* USER CODE BEGIN 2 */
  HAL_UART_Receive_DMA(&huart4, (uint8_t*)base_buff, 12);

  float time_cur;
  float time_las = 0.0;
  uint16_t x_in, y_in;
  int step = 0;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  HAL_TIM_Base_Start_IT(&htim9);
  while (1) {
	  time_cur = (float)ss + (float)ms / 1000.0;

	  //message analysis
	  if (in1 == 1) {
		  //serial monitor
		  HAL_UART_Transmit_DMA(&huart3, (uint8_t*)base_buff, 12);

		  //error check
		  if (base_buff[0] == S1 && base_buff[1] == S2) {
			  if (base_buff[3] == 0x01) {
				  x_in = ((uint16_t)base_buff[4] << 8) | base_buff[5];
				  y_in = ((uint16_t)base_buff[6] << 8) | base_buff[7];
			  }
		  }
		  in1 = 0;
	  }

	  //stepper polling loop
	  if (time_cur - time_las >= 0.005) {
		  time_las = time_cur;
		  //HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_0);
		  //HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_7);
		  //HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_14);

		  if (x_in > XUP) {//output cw rotation
			  HAL_GPIO_WritePin(GPIOD, GPIO_PIN_0, 0);
			  HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_1);


		  } else if (x_in < XDN) {//output ccw rotation
			  HAL_GPIO_WritePin(GPIOD, GPIO_PIN_0, 1);
			  HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_1);


		  }

		  if (y_in > YUP) {//output extend
			  HAL_GPIO_WritePin(GPIOD, GPIO_PIN_2, 0);
			  HAL_GPIO_WritePin(GPIOD, GPIO_PIN_4, 0);
			  HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_3);
			  if (step == 2) {
				  HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_5);
				  step = 0;
			  }
			  step++;


		  } else if (y_in < YDN) {//output retract
			  HAL_GPIO_WritePin(GPIOD, GPIO_PIN_2, 1);
			  HAL_GPIO_WritePin(GPIOD, GPIO_PIN_4, 1);
			  HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_3);
			  if (step == 2) {HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_5);}
			  step--;
			  if (step != 0 && step != 1 && step != 2) {step = 2;}


		  }
	  }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

